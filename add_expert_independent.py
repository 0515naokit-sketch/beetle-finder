# ── 生態学的パラメータ生成 ───────────────────────────────────────────────────────
def tree_for_elev_region(elev, region):
    if elev > 1600:
        return {"buna":0.55,"mizunara":0.30,"urajirogashi":0.10,"konara":0.05}
    elif elev > 1200:
        if region in ["tohoku","hokkaido"]:
            return {"mizunara":0.45,"buna":0.35,"konara":0.15,"kunugi":0.05}
        return {"buna":0.35,"mizunara":0.35,"konara":0.20,"kunugi":0.10}
    elif elev > 800:
        if region in ["tohoku","hokkaido"]:
            return {"konara":0.40,"mizunara":0.35,"buna":0.15,"kunugi":0.10}
        return {"konara":0.40,"mizunara":0.25,"kunugi":0.20,"buna":0.15}
    elif elev > 400:
        if region in ["kinki","chugoku","shikoku","kyushu"]:
            return {"konara":0.38,"kunugi":0.32,"mizunara":0.15,"yanagi":0.10,"hannoki":0.05}
        return {"konara":0.42,"kunugi":0.30,"mizunara":0.18,"hannoki":0.10}
    else:
        return {"kunugi":0.50,"konara":0.28,"yanagi":0.12,"hannoki":0.10}

def best_species_for(elev, region, trees):
    sp = []
    if elev >= 500: sp.append("miyama")
    if elev >= 700: sp.append("akaashi")
    if elev < 1200: sp.append("nokogiri")
    if elev < 600:  sp.append("kabuto")
    if trees.get("yanagi",0) > 0.10: sp.append("hirata")
    if not sp: sp = ["nokogiri","kokuwagata"]
    random.shuffle(sp)
    return sp[:3]

def best_months_for(elev, region):
    if elev >= 1200: return [6,7]
    if elev >= 600:  return [6,7,8]
    if region in ["tohoku","hokkaido"]: return [7,8]
    return [7,8]

SLOPES = ["N","NE","E","SE","S","SW","W","NW"]

def lp_for_elev(elev):
    return max(0, min(5, 5 - elev//400))

# ── 1°あたりのkm換算（日本緯度帯） ─────────────────────────────────────────────
KM_PER_LAT = 111.0
def km_per_lng(lat_deg):
    return 111.0 * math.cos(math.radians(lat_deg))

# ── グリッド生成 ─────────────────────────────────────────────────────────────────
new_spots = []

for (aname, region, lat_min, lat_max, lng_min, lng_max,
     elev_lo, elev_hi, step_km, pref_hint) in MOUNTAIN_AREAS:

    dlat = step_km / KM_PER_LAT
    mid_lat = (lat_min + lat_max) / 2
    dlng = step_km / km_per_lng(mid_lat)

    lat = lat_min + dlat * 0.5
    while lat < lat_max:
        lng = lng_min + dlng * 0.5
        while lng < lng_max:
            # 実際の標高をエリアの標高範囲から決定（緯度経度ベースのノイズで変化）
            # 山の中心ほど高く、端は低め
            cx = (lng - lng_min) / max(lng_max - lng_min, 0.001)
            cy = (lat - lat_min) / max(lat_max - lat_min, 0.001)
            # 山岳感: 端から中心にかけて徐々に高く
            center_factor = 1.0 - 2.0 * max(abs(cx-0.5), abs(cy-0.5))
            center_factor = max(0.0, min(1.0, center_factor))
            # ランダムノイズを加える
            noise = random.uniform(-0.15, 0.15)
            t = max(0.0, min(1.0, center_factor * 0.7 + 0.15 + noise))
            elev = int(elev_lo + t * (elev_hi - elev_lo))

            slope = random.choice(SLOPES)
            trees = tree_for_elev_region(elev, region)
            water = random.choice([30,50,80,120,180,250,400,600])
            lp = lp_for_elev(elev)
            best_sp = best_species_for(elev, region, trees)
            best_m  = best_months_for(elev, region)
            access_hard = 2 if elev > 800 else (3 if elev > 1300 else 1)

            # スポット名
            sp_label = best_sp[0] if best_sp else "nokogiri"
            sp_ja = {"miyama":"ミヤマ","akaashi":"アカアシ","nokogiri":"ノコギリ",
                     "hirata":"ヒラタ","kabuto":"カブト","kokuwagata":"コクワ"}
            sl_tag = ["北面林道","南斜面","尾根沿い","谷間沢沿い","高台コナラ帯","林道分岐点"]
            name = f"{aname}・{random.choice(sl_tag)}"

            new_spots.append({
                "id": eid,
                "name": name,
                "lat": round(lat + random.uniform(-0.003,0.003), 4),
                "lng": round(lng + random.uniform(-0.004,0.004), 4),
                "prefecture": pref_hint,
                "region": region,
                "elevation": elev,
                "slope": slope,
                "trees": trees,
                "water_m": water,
                "light_pollution": lp,
                "access_hard": access_hard,
                "best": best_sp,
                "best_months": best_m,
                "independent": True   # 独立予測ポイントフラグ
            })
            eid += 1
            lng += dlng
        lat += dlat

# ── 保存 ─────────────────────────────────────────────────────────────────────────
all_spots = existing + new_spots
with open("static/expert_spots.json","w",encoding="utf-8") as f:
    json.dump({"spots": all_spots}, f, ensure_ascii=False, indent=None)

