files = sorted(glob.glob('/Users/naokitakahashi/beetle-finder/templates/guide_pref_*.html'))
updated = 0

for fp in files:
    m = re.search(r'guide_pref_(\w+)\.html', fp)
    if not m:
        continue
    slug = m.group(1)
    pref_name = PREF_NAMES.get(slug, slug)
    data = PREF_UNIQUE.get(slug)
    if not data:
        print(f'  SKIP (no data): {slug}')
        continue

    with open(fp, 'r', encoding='utf-8') as f:
        content = f.read()

    if 'author-note' in content:
        print(f'  SKIP (already added): {slug}')
        continue

    # CSS追加
    content = content.replace('</style>', CSS_UNIQUE + '\n  </style>', 1)

    # 挿入するHTML
    unique_html = f'''
<div class="author-note">
  <p>「{data["author_comment"]}」（著者・森山春樹）</p>
</div>

<div class="caution-unique">
  <h4>⚠️ {pref_name}の採集で注意すること</h4>
  <p>{data["caution"]}</p>
</div>'''

    # .cta-boxの直前に挿入
    content = re.sub(
        r'(<div class=["\']cta-box["\'])',
        unique_html + r'\n\1',
        content,
        count=1
    )

    with open(fp, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f'  OK: {pref_name}')
    updated += 1

print(f'\n完了: {updated}/{len(files)} 件更新')
