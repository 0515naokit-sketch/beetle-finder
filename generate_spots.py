# 各家族スポットから3-6件の精密サブスポットを生成

SLOPE_DIRS = ["N","NE","NW","E","W","SE","SW","S"]

def tree_preset(elev, region, slope):
    """標高・地域・斜面方向から樹種比率を推定"""
    if elev >= 1500:
        return {"buna":50,"mizunara":30,"konara":10,"other":10}
    elif elev >= 1000:
        if region in ["hokkaido","tohoku"]:
            return {"buna":45,"mizunara":35,"konara":10,"other":10}
        return {"buna":40,"mizunara":30,"konara":20,"other":10}
    elif elev >= 700:
        if slope in ["N","NE","NW"]:
            return {"buna":35,"konara":30,"mizunara":20,"kunugi":5,"other":10}
        return {"konara":40,"kunugi":20,"buna":20,"mizunara":10,"other":10}
    elif elev >= 400:
        if slope in ["N","NE"]:
            return {"konara":45,"kunugi":20,"buna":15,"sugi":10,"other":10}
        return {"kunugi":40,"konara":35,"sugi":10,"other":15}
    elif elev >= 200:
        return {"kunugi":45,"konara":30,"hannoki":10,"other":15}
    else:
        return {"kunugi":40,"konara":25,"yanagi":20,"hannoki":10,"other":5}

def water_dist(base_water, variant):
    """水場距離（地形バリエーション）"""
    d = base_water + variant * 80
    return max(20, min(800, d + random.randint(-30,30)))

def light_pol(elev, density):
    """光害レベル（1=暗い〜5=明るい）"""
    if elev >= 1200: return 1
    elif elev >= 800: return min(2, density)
    elif elev >= 500: return min(3, density)
    elif elev >= 200: return density
    return min(5, density + 1)

def make_expert_spot(eid, family_spot, variant_idx):
    """家族スポットから玄人サブスポットを生成"""
    fs = family_spot
    slope = SLOPE_DIRS[(variant_idx * 3 + eid) % len(SLOPE_DIRS)]
    elev_offset = [-150,-80,0,50,150,250][variant_idx % 6]
    elev = max(50, fs["elevation"] + elev_offset)

    # 座標をランダムにオフセット（1〜5km程度）
    lat_off = random.uniform(-0.03, 0.03)
    lng_off = random.uniform(-0.04, 0.04)
    lat = round(fs["lat"] + lat_off, 4)
    lng = round(fs["lng"] + lng_off, 4)

    trees = tree_preset(elev, fs["region"], slope)
    water = water_dist(100, variant_idx)

    density_map = {1:1, 2:2, 3:3}
    lp = light_pol(elev, density_map.get(fs["difficulty"],2))

    # サブ名称
    sub_names = ["北面コナラ帯","南斜面クヌギ林","谷間樹液帯","尾根沿いミズナラ","渓流沿いヤナギ帯","高台コナラ林"]
    sub_name = f"{fs['name']}・{sub_names[variant_idx % len(sub_names)]}"

    best_sp = []
    if "miyama" in fs["species"] and elev >= 400: best_sp.append("miyama")
    if "akaashi" in fs["species"] and elev >= 600: best_sp.append("akaashi")
    if "nokogiri" in fs["species"]: best_sp.append("nokogiri")
    if "hirata" in fs["species"] and water < 150: best_sp.append("hirata")
    if "kabuto" in fs["species"] and elev < 800: best_sp.append("kabuto")
    if not best_sp: best_sp = ["nokogiri","kokuwagata"]

    return {
        "id": eid,
        "name": sub_name,
        "lat": lat,
        "lng": lng,
        "prefecture": fs["prefecture"],
        "region": fs["region"],
        "elevation": elev,
        "slope": slope,
        "trees": trees,
        "water_m": water,
        "light_pollution": lp,
        "access_hard": min(3, fs["difficulty"] + (1 if variant_idx >= 3 else 0)),
        "best": best_sp[:3],
        "best_months": fs["best_months"]
    }

